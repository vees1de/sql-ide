class ChartSpec(BaseModel):
    chart_type: str
    title: str
    encoding: ChartEncoding
    options: dict[str, Any] = Field(default_factory=dict)
    data: dict[str, Any] | None = None
    variant: str | None = None
    explanation: str | None = None
